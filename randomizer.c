// apply or remove randomization, in place, to reduce spectral features of the data
void randomize_ovp_frame(uint8_t *buf) {
	for (int i=0; i < OVP_SINGLE_FRAME_SIZE; i++) {
		buf[i] ^= ccsds_randomization_sequence[i];
	}
}
